"""
test_lob_letter.py — Send ONE test letter via Lob.com to verify setup.

With the test API key the letter is NOT actually mailed — Lob processes it,
returns a preview PDF URL, and charges nothing. Swap to the live key once
you've confirmed the letter looks right.

Usage:
    python test_lob_letter.py

The letter will be sent to Will Workman at the HOA address as a self-test.
Check the printed URL or log into https://dashboard.lob.com to see the PDF preview.
"""

