if not LOB_API_KEY:
    print("❌ LOB_API_KEY not found in .env — check your .env file.")
    exit(1)

key_type = "TEST" if LOB_API_KEY.startswith("test_") else "LIVE"
print(f"Using {key_type} Lob API key")
print(f"Sending test letter to: {TEST_NAME}, {TEST_LINE1}, {TEST_CITY}, {TEST_STATE} {TEST_ZIP}")
print("...")

resp = requests.post(
    "https://api.lob.com/v1/letters",
    auth=(LOB_API_KEY, ""),
    data={
        "to[name]":              TEST_NAME,
        "to[address_line1]":     TEST_LINE1,
        "to[address_city]":      TEST_CITY,
        "to[address_state]":     TEST_STATE,
        "to[address_zip]":       TEST_ZIP,
        "to[address_country]":   "US",
        "from[name]":            HOA_NAME,
        "from[address_line1]":   HOA_LINE1,
        "from[address_city]":    HOA_CITY,
        "from[address_state]":   HOA_STATE,
        "from[address_zip]":     HOA_ZIP,
        "from[address_country]": "US",
        "file":                  html,
        "color":                 "false",
        "double_sided":          "false",
        "mail_type":             "usps_first_class",
        "use_type":              "operational",
    },
    timeout=30,
)

if resp.status_code in (200, 201):
    result    = resp.json()
    letter_id = result.get("id", "unknown")
    url       = result.get("url", "")
    status    = result.get("status", "")
    print(f"\n✅ Success!")
    print(f"   Letter ID : {letter_id}")
    print(f"   Status    : {status}")
    if url:
        print(f"   Preview   : {url}")
    print(f"\n{'='*60}")
    print("Next steps:")
    print("  1. Open the Preview URL above to verify the letter layout")
    print("  2. Upload your Word doc letter templates so we can add real body text")
    print("  3. Once layout is confirmed, swap .env to the live_ API key")
    print(f"{'='*60}")
else:
    print(f"\n❌ Lob API error {resp.status_code}:")
    print(resp.text)
